const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const deterministicUUID = (seedString) => {
  const hash = crypto.createHash('md5').update(seedString).digest('hex');
  return `${hash.slice(0, 8)}-${hash.slice(8, 12)}-4${hash.slice(13, 16)}-a${hash.slice(17, 20)}-${hash.slice(20, 32)}`;
};

const escapeSql = (str) => (str == null ? '' : String(str).replace(/'/g, "''"));
const sqlString = (str) => `'${escapeSql(str)}'`;
const sqlJsonb = (value) => `'${escapeSql(JSON.stringify(value))}'::jsonb`;

const PROVINCES = [
  { name: 'Hà Nội', stay: 15, exp: 5, svc: 5 },
  { name: 'Hồ Chí Minh', stay: 15, exp: 5, svc: 5 },
  { name: 'Đà Nẵng', stay: 15, exp: 10, svc: 5 },
  { name: 'Quảng Ninh', stay: 10, exp: 10, svc: 5 },
  { name: 'Lào Cai', stay: 10, exp: 5, svc: 5 },
  { name: 'Ninh Bình', stay: 10, exp: 5, svc: 5 },
  { name: 'Quảng Nam', stay: 10, exp: 5, svc: 5 },
  { name: 'Thừa Thiên Huế', stay: 5, exp: 5, svc: 5 },
  { name: 'Kiên Giang', stay: 10, exp: 5, svc: 5 },
  { name: 'Khánh Hòa', stay: 10, exp: 5, svc: 5 },
];

const SVC_TYPES = ['SPA', 'MASSAGE', 'CHEF', 'PHOTOGRAPHY', 'MAKEUP'];
const START_DATE = process.env.SEED_START_DATE || '2026-06-04';
const DAYS_AHEAD = Number(process.env.SEED_DAYS || 90);

const addDays = (date, days) => {
  const next = new Date(`${date}T00:00:00.000Z`);
  next.setUTCDate(next.getUTCDate() + days);
  return next.toISOString().slice(0, 10);
};

const listingCategory = (category, index) => {
  if (category === 'SVC') {
    return `SVC_${SVC_TYPES[index % SVC_TYPES.length]}`;
  }
  return category;
};

const defaultQuantity = (category, index) => {
  if (category === 'STAY') return 3 + (index % 5);
  if (category === 'EXP') return 8 + (index % 8);
  return 2 + (index % 4);
};

const scheduleConfig = (category, quantity) => {
  if (category === 'STAY') {
    return {
      defaultQuantity: quantity,
      timeSlots: [],
    };
  }

  return {
    defaultQuantity: quantity,
    timeSlots: [
      { slot: 'ALL_DAY', quantity },
      { slot: '08:00 - 10:00', quantity },
      { slot: '14:00 - 16:00', quantity },
    ],
  };
};

let sql = `-- Seed Inventory Data for Catalog demo listings\n`;
sql += `-- Generated by BookingandInventory/scripts/generate-inventory-seed.js\n`;
sql += `-- Default start date: ${START_DATE}. Override with SEED_START_DATE=YYYY-MM-DD.\n`;
sql += `SET statement_timeout = 0;\n\n`;

const listings = [];
for (const province of PROVINCES) {
  for (const category of ['STAY', 'EXP', 'SVC']) {
    const count = category === 'STAY' ? province.stay : category === 'EXP' ? province.exp : province.svc;
    for (let i = 0; i < count; i++) {
      listings.push({
        id: deterministicUUID(`listing_${province.name}_${category}_${i}`),
        category: listingCategory(category, i),
        baseCategory: category,
        province: province.name,
        index: i,
      });
    }
  }
}

for (const listing of listings) {
  const quantity = defaultQuantity(listing.baseCategory, listing.index);
  const configId = deterministicUUID(`inventory_config_${listing.id}`);
  const config = scheduleConfig(listing.baseCategory, quantity);

  sql += `INSERT INTO public.inventory_configs (id, created_at, updated_at, listing_id, category, schedule_config, is_active) VALUES `;
  sql += `(${sqlString(configId)}, NOW(), NOW(), ${sqlString(listing.id)}, ${sqlString(listing.category)}, ${sqlJsonb(config)}, true)\n`;
  sql += `ON CONFLICT (listing_id) DO UPDATE SET category = EXCLUDED.category, schedule_config = EXCLUDED.schedule_config, is_active = EXCLUDED.is_active, updated_at = NOW();\n`;

  for (let day = 0; day < DAYS_AHEAD; day++) {
    const date = addDays(START_DATE, day);
    const isSoldOut = listing.baseCategory === 'STAY' && listing.index % 9 === 0 && day % 17 === 0;
    const status = isSoldOut ? 'SOLD_OUT' : 'AVAILABLE';
    const availableQuantity = isSoldOut ? 0 : quantity;
    const calendarId = deterministicUUID(`inventory_calendar_${listing.id}_${date}_ALL_DAY`);

    sql += `INSERT INTO public.inventory_calendars (id, created_at, updated_at, listing_id, date, time_slot, available_quantity, status, version) VALUES `;
    sql += `(${sqlString(calendarId)}, NOW(), NOW(), ${sqlString(listing.id)}, ${sqlString(date)}, 'ALL_DAY', ${availableQuantity}, ${sqlString(status)}, 0)\n`;
    sql += `ON CONFLICT (listing_id, date, time_slot) DO UPDATE SET available_quantity = EXCLUDED.available_quantity, status = EXCLUDED.status, updated_at = NOW();\n`;
  }
}

sql += `\n-- Verification Block\n`;
sql += `DO $$\n`;
sql += `DECLARE\n`;
sql += `  config_count integer;\n`;
sql += `  calendar_count integer;\n`;
sql += `  available_next_month_count integer;\n`;
sql += `BEGIN\n`;
sql += `  SELECT COUNT(*) INTO config_count FROM public.inventory_configs WHERE is_active = true;\n`;
sql += `  SELECT COUNT(*) INTO calendar_count FROM public.inventory_calendars WHERE date >= ${sqlString(START_DATE)}::date AND date < (${sqlString(START_DATE)}::date + INTERVAL '${DAYS_AHEAD} days');\n`;
sql += `  SELECT COUNT(DISTINCT listing_id) INTO available_next_month_count FROM public.inventory_calendars WHERE status = 'AVAILABLE' AND available_quantity > 0 AND date >= (${sqlString(START_DATE)}::date + INTERVAL '30 days') AND date < (${sqlString(START_DATE)}::date + INTERVAL '60 days');\n`;
sql += `  IF config_count < ${listings.length} THEN\n`;
sql += `    RAISE EXCEPTION 'Inventory seed verification failed: only % active configs', config_count;\n`;
sql += `  END IF;\n`;
sql += `  IF calendar_count < ${listings.length * DAYS_AHEAD} THEN\n`;
sql += `    RAISE EXCEPTION 'Inventory seed verification failed: only % calendar rows', calendar_count;\n`;
sql += `  END IF;\n`;
sql += `  IF available_next_month_count < ${Math.floor(listings.length * 0.8)} THEN\n`;
sql += `    RAISE EXCEPTION 'Inventory seed verification failed: only % listings available next month', available_next_month_count;\n`;
sql += `  END IF;\n`;
sql += `  RAISE NOTICE 'Inventory seed verification passed: configs=%, calendars=%, available next month=%', config_count, calendar_count, available_next_month_count;\n`;
sql += `END $$;\n`;

const destDir = path.join(__dirname);
const destFile = path.join(destDir, 'seed-inventory.sql');
fs.writeFileSync(destFile, sql, 'utf8');

console.log(
  `Generated inventory seed: ${listings.length} listings x ${DAYS_AHEAD} days -> ${destFile}`,
);
